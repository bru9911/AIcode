# Estimativas aproximadas
estrelas_via_lactea = 100e9  # 100 bilhões de estrelas na Via Láctea
galaxias_observaveis = 2e12  # 2 trilhões de galáxias no universo observável

# Calcular o número total de estrelas no universo
estrelas_universo = estrelas_via_lactea * galaxias_observaveis

# Exibir resultado
print(f"Estimativa do número total de estrelas no universo: {estrelas_universo:.2e}")
